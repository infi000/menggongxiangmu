function createDom_jobList() {
    var html = "";
    for (var i = 0; i < JOBDATA.length; i++) {
        var li = JOBDATA[i],
            name = li['name'],
            className = (i == 0) ? 'f-c-main' : '';
        html += '<li><a class="' + className + ' btn-chooseJob" datatype="' + i + '">' + name + '</a></li>'
    };
    $("#job-list").html(html);
};


function createDom_jobTable(num) {
    var data = JOBDATA[num];
    $("#job-name").html(data['name']);
    $("#job-addr").html(data['addr']);
    $("#job-tagname").html(data['tag']);
    $("#job-way").html(data['way']);
    $("#job-desc").html(data['desc']);
    $("#job-require").html(data['require']);
};



function event() {
    $('body').on("click", ".btn-chooseJob", function(e) {
        console.log(e)
        var num = $(this).attr('datatype');
        createDom_jobTable(num);
        $(".btn-chooseJob").attr('class', 'btn-chooseJob');
        $(this).attr('class', 'f-c-main btn-chooseJob')
    })
};


$(document).ready(function() {
    createDom_jobList();
    createDom_jobTable(0);
    event();
});